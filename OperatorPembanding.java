public class OperatorPembanding
{
	public static void main(String [] args)
	{
		int nilaiA,nilaiB;
		boolean hasil;


		//input data variabel
		nilaiA = 10;
		nilaiB = 8;

		// A lebih besar dari B

		System.out.println("10 lebih besar dari 8 ?");
		hasil = nilaiA > nilaiB;
		System.out.println(hasil);

		// A lebih besar dari B

		System.out.println("10 lebih kecil dari 8 ?");
		hasil = nilaiA < nilaiB;
		System.out.println(hasil);
	}
}